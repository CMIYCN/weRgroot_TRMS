--deletes employee by username
CREATE OR REPLACE PROCEDURE DELETE_EMPLOYEE(
EMP_USERNAME IN VARCHAR2
)
AS
BEGIN
DELETE FROM EMPLOYEE WHERE EMPLOYEE.USERNAME = EMP_USERNAME;
END DELETE_EMPLOYEE;
